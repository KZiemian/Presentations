class Main {
    public static void main(String[] args) {
	int intVar1 = 0;

	System.out.print("In the footsteps of languages like C and C++, " +
			 "Java also has incrementation " +
			 "and decrementation operators.\n\n");

	System.out.print("intVar1 == " + intVar1 + ".\n\n");


	System.out.println("Instruction: intVar1++;");
	intVar1++;

	System.out.print("intVar1 == " + intVar1 + ".\n\n");


	System.out.println("Instruction: intVar1++;");
	intVar1++;

	System.out.print("intVar1 == " + intVar1 + ".\n\n");


	System.out.println("Instruction: intVar1++;");
	intVar1++;

	System.out.print("intVar1 == " + intVar1 + ".\n\n");


	System.out.println("Instruction: intVar1--;");
	intVar1--;

	System.out.print("intVar1 == " + intVar1 + ".\n\n");


	System.out.println("Instruction: ++intVar1;");
	++intVar1;

	System.out.print("intVar1 == " + intVar1 + ".\n\n");


	System.out.println("Instruction: --intVar1;");
	--intVar1;

	System.out.print("intVar1 == " + intVar1 + ".\n\n");


	System.out.println("Using these operators with integer " +
			   "variables is perfectly good. If you use " +
			   "them with floating-point variables, " +
			   "you do it on your own responsibility.");
    }
}
