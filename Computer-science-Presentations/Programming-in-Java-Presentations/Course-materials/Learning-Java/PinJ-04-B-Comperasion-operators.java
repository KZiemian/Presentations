class Main {
    public static void main(String[] args) {
	System.out.print("We will now look at a few more examples of " +
			 "comparison operators.\n\n");


	System.out.println("Operator of equality: ==.");
	System.out.println("(0 == 0) == " + (0 == 0) + ".");
	System.out.print("(0 == 1) == " + (0 == 1) + ".\n\n");


	System.out.println("Operator of inequality: !=.");
	System.out.println("(0 != 0) == " + (0 != 0) + ".");
	System.out.print("(0 != 1) == " + (0 != 1) + ".\n\n");


	System.out.println("Operator \"bigger\": <.");
	System.out.println("(0 < 0) == " + (0 < 0) + ".");
	System.out.println("(0 < 1) == " + (0 < 1) + ".");
	System.out.print("(1 < 0) == " + (1 < 0) + ".\n\n");


	System.out.println("Operator \"bigger or equal\": <=.");
	System.out.println("(0 <= 0) == " + (0 <= 0) + ".");
	System.out.println("(0 <= 1) == " + (0 <= 1) + ".");
	System.out.print("(1 <= 0) == " + (1 <= 0) + ".\n\n");


	System.out.println("Operator \"lesser\": >.");
	System.out.println("(0 > 0) == " + (0 > 0) + ".");
	System.out.println("(0 > 1) == " + (0 > 1) + ".");
	System.out.print("(1 > 0) == " + (1 > 0) + ".\n\n");


	System.out.println("Operator \"lesser or equal\": >=.");
	System.out.println("(0 >= 0) == " + (0 >= 0) + ".");
	System.out.println("(0 >= 1) == " + (0 >= 1) + ".");
	System.out.println("(1 >= 0) == " + (1 >= 0) + ".");
    }
}
