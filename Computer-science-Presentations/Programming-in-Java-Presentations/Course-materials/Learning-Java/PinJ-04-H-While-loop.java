// While loop in Java is quite straightforward.

class Main {
    public static void main(String[] args) {
	int intVar1 = 0;

	// Like in C and C++, while loops execute its body when loop
	// condition is true.
	while (intVar1 < 5) {
	    System.out.println("intVar1 == " + intVar1 + ".");

	    // Like always, remember to check when the loop stops
	    // and add appropriate code.
	    intVar1++;
	}
    }
}
